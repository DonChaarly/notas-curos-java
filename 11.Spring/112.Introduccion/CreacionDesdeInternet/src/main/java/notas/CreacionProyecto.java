package notas;

public class CreacionProyecto {
    
    /*
    
                Creacion de proyecto utilizando la pagina oficial de spirng
    
    Se entrar a start.spirng.io
    Se observan las diferentes caracteristicas que se puede elegir
        project: se esoge la tecnologia para administra las dependencias
        lenguage: se escoge el lenguaje para programar
        SpringBoot: Se escoge la version de springboot, escoge la que te recomienda la mas estable
        ProjectMetadata: los metadatados del proyecto
            Se introducen los valores que prefieras
            packaging: Se indica el tipo de paquete, ya no es necesario crear aplicaciones de tipo war
            java: Se escoge la version de java que se este utilizando
    
        Dependencias: con maven o gradel se puede administrar las dependencias de 
            java y springboot nos agrega paquetes de librerias podemos seleccionar facilmente
            Las librerias que se agregaron en este proyecto fueron:
            Spring Boot DevTools: nos permite reiniciar mas facilmente nuestro servidor de aplicaciones embebido de tomcat
            Lombok: nos permite reducir codigo como los metodos get y set y agrega  de manera automatica el manejo de log4j 
            SpringWeb: nos permite crear aplicaciones de tipo REST
            Thymeleaf: Nos permite agregar paginas web al proyecto de manera mas simple, ya no se usa jsp
    Se genera el archicvo y se descarga
    Se descomprime
    dentro se tienen todos los archivos necesarios
    los archivos de mvnw.cmd nos permite desplegar la aplicacion sin tener instalado maven
    Dentro de las carpetas tambien se tiene la carpeta de resourcers donde se teie la carpetas de: 
        static: aqui se pueden agregar archivos staticos como archivos de javaScrip, archivos css o imagenes
        templates: se pueden agregar archivos utilizando thymeleaf
        application.properties: aqui es donde podemos modificar la configuracion del framework de spring y springboot
    
    */
    
}

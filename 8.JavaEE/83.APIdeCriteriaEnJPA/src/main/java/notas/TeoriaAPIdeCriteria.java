package notas;

public class TeoriaAPIdeCriteria {
    
    /*
    
                                API de Criteria JPA
    
    Para evitar la concatenacion de muchos quieries dinamicos o parametros concatenados en la cadena JPQL
    JPA ofrecer el API de criteria
    Con esta API es posible construir consultas dinamicas complejas permitiendo configurar desde el mismo 
    lenguaje de programacion los filtros necesarios para la consulta utilizando el lenguaje JAVA
    
    API de criteria:
    El api de criteria es una alternativa al uso de JPQL o SQL Nativo
    Permite la combinación de campos de criterio complejos (ej. Una pantalla de busqueda avanzada)
    Permite crear queries dinamicos complejos mas facilmente, evitando la concatenacion de cadenas.
    
                    Caracteristicas del API de criteria
    Son estrictos en codigo JAVA
    Son typeSave (parámetros revisados en tiempo de compilación)
    Son queries portables (funcionan en cualquier base de datos)
    Se utilizan clases Java en lugar de cadenas JPQL o SQL
    Permite utilizar expresiones, joins, ordenamientos, etc.
        
    
    
    */
    
}

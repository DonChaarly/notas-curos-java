package enumeracion;

/*Una enumeracion es como una clase pero particular 
  que contiene varios elementos
*/

//En vez de poner class se coloca enum
public enum Dias {
    //Los atributos siempre son public static y final, osea constantes
    LUNES,
    MARTES,
    MIERCOLES,
    JUEVES,
    VIERNES,
    SABADO,
    DOMINGO
    //La enumeracion nos permite crear mas facilmente este tipo de datos
}
